	function getRectangle($dept) {
		$tab = $this->getPolygone($dept);
		// Recherche xmin et xmax
		$xmax = $tab[0];
		$xmin = $tab[0];
		for ($i = 0; $i < count($tab)-1 ; $i+=2) {
			if ($tab[$i]>$xmax) $xmax = $tab[$i];
			if ($tab[$i]<$xmin) $xmin = $tab[$i];
		}
		// Recherche ymin et ymax
		$ymax = $tab[1];
		$ymin = $tab[1];
		for ($i = 1; $i < count($tab) ; $i+=2) {
			if ($tab[$i]>$ymax) $ymax = $tab[$i];
			if ($tab[$i]<$ymin) $ymin = $tab[$i];
		}
		$rect = array($xmin,$ymin,$xmax,$ymax);
		return $rect;
	}

	function alertMe($dept) {
		if ($this->img_ok == true) {
			$alertMe = false;
			// Recuperation des couleurs vert,jaune,orange,rouge dans la légende de la carte
			$vert_index = imagecolorat($this->img, 23, 231); 
			$jaune_index = imagecolorat($this->img, 23, 186);
			$orange_index = imagecolorat($this->img, 23, 141);
			$rouge_index = imagecolorat($this->img, 23, 85);
			$couleurs = array($vert_index => 1,	$jaune_index => 2, $orange_index => 3, $rouge_index => 4);
			list($xmin,$ymin,$xmax,$ymax) = $this->getRectangle($dept);
			
			// On recherche une des 4 couleurs prédéfinies
			// Sens horizontal de recherche
			$x = $xmin + ($xmax-$xmin) / 2;
			$y = $ymin + ($ymax-$ymin) / 2;
			while (($x <= $xmax) && ($alertMe == false))
			{
				$color_index = imagecolorat($this->img,$x,$y);
				if (array_key_exists($color_index,$couleurs)) { $alertMe = $couleurs[$color_index]; }
				$x++;
			}
			
			// Sens vertical de recherche
			$x = $xmin + ($xmax-$xmin) / 2;
			$y = $ymin + ($ymax-$ymin) / 2;
			
			while (($y <= $ymax) && ($alertMe == false))
			{
				$color_index = imagecolorat($this->img,$x,$y);
				if (array_key_exists($color_index,$couleurs)) { $alertMe = $couleurs[$color_index]; }
				$y++;
			}
			
		}else{
			$alerteMe = 0;
		}
		return $alertMe;
